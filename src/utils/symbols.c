#include "minishell.h"

int	ft_is_symbol(char c)
{
	char	*set;
	int		i;

	i = 0;
	set = "()><|&";
	while (set[i])
	{
		if (c == set[i])
			return (1);
		i++;
	}
	return (0);
}
